//Math Object

//link of documentation about this object
//https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/Math

//if we need at some time some mathematical operations we can use 
//Math object into javascript, here some examples of how to use
//some methods of this object.

//will get a random number
let randomNumber = Math.random();
console.log('random number', randomNumber);

//will round up the nearest integer number
let roundNumber = Math.round(1.9);
console.log('round number', roundNumber);

//will return the maximum value of passed values
let maxNumber = Math.max(1,2,3,4,6,8,40,100);
console.log('max number', maxNumber);

//will return the smallest value of passed values
let minNumber = Math.min(1,2,3,4,6,8,40,100);
console.log('min number', minNumber);